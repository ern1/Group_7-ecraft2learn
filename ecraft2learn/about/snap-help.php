<p>
<a ref="https://snap.berkeley.edu"><span class=" notranslate" translate="no">Snap!</span></a>
is a big brother of <a href="https://scratch.mit.edu" target="_blank">Scratch</a>.
It can do much more than Scratch but is a bit more complex.
It is <a href="https://snap.berkeley.edu/snapsource/help/SnapManual.pdf" target="_blank">documented here.</a>
</p>
</p>
Most importantly it has enabled the <a href="http://project.ecraft2learn.eu/" target="_blank">eCraft2Learn project</a> to add new blocks. These include
<ol>
<li>
the ability to use speech input and output
</li>
<li>
image recognition by connecting to AI cloud services (<a href="https://github.com/ToonTalk/ai-cloud/wiki" target="_blank">free API keys required</a>)
</li>
<li>
the ability to read and write the digital pins of a <a href="https://www.raspberrypi.org/" target="_blank">Raspberry Pi</a> or an <a href="http://www.arduino.org/" target="_blank">Arduino connected to a Raspberry Pi</a>.
Note that this requires the running of a local server on the Raspberry Pi.
</li>
</ol>
