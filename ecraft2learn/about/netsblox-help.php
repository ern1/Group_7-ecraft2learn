<p>
NetsBlox adds several new blocks to <a href="snap-help" target="_blank">Snap!</a> for the purpose of obtaining and processing open data sources.
It also supports real-time collaboration and communication between different NetsBlox instances.
It is <a href="https://netsblox.org/" target="_blank">documented here</a>.
</p>
<p>
The <a href="http://project.ecraft2learn.eu/" target="_blank">eCraft2Learn project</a> is exploring ways this tool can communicate with tools such as <a href="snap4arduino-help.html" target="_blank">Snap4Arduiono</a>.
</p>
<p>
The eCraft2Learn project provides no support or integration for this tool.
