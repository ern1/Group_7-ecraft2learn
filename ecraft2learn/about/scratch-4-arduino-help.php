<p>
<a href="http://s4a.cat/" target="_blank">Scratch for the Arduino</a> is a free app.
It has new blocks for communicating with the <a href="http://www.arduino.org/" target="_blank">Arduino</a> pins.
It is not web-based and programs created cannot be shared at the <a href="https://scratch.mit.edu/" target="_blank">Scratch web site</a>.
</p>
<p>
The <a href="http://project.ecraft2learn.eu/" target="_blank">eCraft2Learn project</a> provides no support or integration for this tool.
</p>
