<p>
<a href="https://www.arduino.cc/en/Main/Software" target="_blank">The Arduino IDE</a> is a free text-based <a href="http://www.arduino.org/" target="_blank">Arduino</a> programming system.
There is both an web version and an app.
C programs are compiled to run directly on an Ardunio.
This means the Ardunio can be embedded in an artefact and run without either being accompanied by a Raspberry Pi or in communication with another computer.
</p>
<p>
The Arduino IDE relies upon C, a textual language that was designed for use by professionals not students.
<a href="arbublock-help.html" target="_blank">ArudBlock</a> is a blocks-based language designed for beginneers that has approximately the same functionality as the Arduino IDE.
</p>
<p>
The <a href="http://project.ecraft2learn.eu/" target="_blank">eCraft2Learn project</a> provides no support or integration for this tool.
