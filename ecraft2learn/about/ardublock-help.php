<p>
<a href="http://blog.ardublock.com/" target="_blank">ArduBlock</a> is a free block-based <a href="http://www.arduino.org/" target="_blank">Arduino</a> programming language.
Unlike <a href="snap-help.html" target="_blank">Snap!</a> and <a href="snap4arduino-help.html" target="_blank">Snap4Arduino</a> it is not web-based but runs as a Java application.
The main unique feature of this programming language is that programs can be compiled to run directly on an Ardunio.
This means the Ardunio can be embedded in an artefact and run without either being accompanied by a Raspberry Pi or in communication with another computer.
</p>

