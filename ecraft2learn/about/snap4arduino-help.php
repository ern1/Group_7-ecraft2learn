<p>
Snap4Arduino adds several new blocks to <a href="snap-help" target="_blank">Snap!</a> for the purpose of controlling <a href="http://www.arduino.org/" target="_blank">Arduinos</a>.
It is <a href="http://snap4arduino.org" target="_blank">documented here</a>.
Note that the web version runs only in Chrome and requires <a href="http://snap4arduino.org/#plugin" target="_blank">the installation of a Chrome extension</a>.
This has already been performed if you are using an eCraft2Learn SD card on a Raspberry Pi.
</p>
<p>
The eCraft2Learn to <a href="snap-help.html" target="_blank">Snap! blocks for communicating with AI cloud services</a> will run in Snap4Arduino
when the site <a href="https://github.com/bromagosa/Snap4Arduino/issues/177" target="_blank">supports HTTPS</a>.
