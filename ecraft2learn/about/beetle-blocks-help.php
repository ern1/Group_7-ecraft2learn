<p>
<a href="beetleblocks.com" target="_blank">BeetleBlocks</a> adds several new blocks to <a href="snap-help" target="_blank">Snap!</a> for the purpose of programmatically generating 3D models.
Generated models can be saved as STL or OBJ files suitable for 3D printing.
The alternative approach to creating models is via direct manipulation of the interface. See <a href="tinkercad-help.html" target="_blank">Tinkercad</a>.
</p>
<p>
The eCraft2Learn project provides no support or integration for this tool.
