<p>
Pocket Code is a free blocks-based programming language that runs as an app on Android tablets and smart phones.
A version for iPhones and iPads is under development.
The authoring, testing, and sharing is performed on a smart phone or tablet.
It is <a href="https://www.catrobat.org/" target="_blank">documented here</a>.
Blocks specialized to communicate with Arduinos via BlueTooth are included.
Note that some Arduinos support BlueTooth and a shield can be added to add this functionaliy.
</p>
<p>
The <a href="http://project.ecraft2learn.eu/" target="_blank">eCraft2Learn project</a> provides no support or integration for this tool.
</p>