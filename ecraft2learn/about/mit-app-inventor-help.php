<p>
MIT App Inventor is a free blocks-based programming language for Android tablets and smart phones.
Authoring Apps takes place in a browser on a PC.
It is <a href="http://appinventor.mit.edu/explore/" target="_blank">documented here</a>.
Apps can be constructed that communicate via BlueTooth or Wifi to a <a href="https://www.raspberrypi.org/" target="blank">Raspberry Pi</a> or an <a href="http://www.arduino.org/" target="blank">Arduino</a>.
Raspberry Pis include BlueTooth and Wifi while some Arduinos do and a shield can be added to add such functionaliy.
</p>
