<p>
<a href="https://www.tinkercad.com/" target="_blank">Tinkercad</a> is a free web-based app for creating 3D models suitable for 3D printing.
Users create models via the Tinkercad interface, in constrast to <a href="beetleblocks-help.html" target="_blank">BeetleBlocks</a> which supports creating programs that construct 3D models.
</p>
<p>
Tinkercad can also be used to <a href="https://www.tinkercad.com/circuits">construct electronic circuits connected to Arduinos and simulate their running</a>.
</p>
<p>
The eCraft2Learn project is investigating the possibility of providing support and integration of this tool.
