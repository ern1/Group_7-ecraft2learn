<p>
<a href="https://www.wolfram.com/mathematica/" target="_blank">Mathematica</a> is a commericial programming language that is free and included in the standard <a href="https://www.raspberrypi.org/" target="_blank">Raspberry Pi</a> release.
It has facilties for communicating with the Raspberry Pi pins and with a <a href="http://reference.wolfram.com/language/ref/device/Arduino.html" target="_blank">connected Arduino</a>.
It has support for machine learning including a <a href="http://blog.stephenwolfram.com/2017/05/machine-learning-for-middle-schoolers/" target="_blank">chapter in a high school text book</a> with many project ideas.
It also provides easy access to a large variety of open data sources.
<a href="http://reference.wolfram.com/language/ref/format/STL.html" target="_blank">3D models suitable for printing can also be created in Mathematica</a>, though in many cases doing so requires a high-level of mathematical sophistication.
</p>
